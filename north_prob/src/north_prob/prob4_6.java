package north_prob;

import java.util.Scanner;
public class prob4_6 {
	public static void main(String[] args) {
		int wins = 0;
		try(Scanner scan = new Scanner(System.in)){
			for (int i = 0; i < 10; i++) {
				int num = scan.nextInt();
				if(num == 1) {
					wins += 1;
				}
			}
			System.out.println("勝ち：" + wins + "負け：" + (10 - wins));
		}
	}
}
